#include "libft.h"
#include <stdlib.h>

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*s1;
	unsigned char	*s2;

	s1 = (unsigned char *)dest;
	s2 = (unsigned char *)src;
	if (dest > src)
	{
		s1 = s1 + n;
		s2 = s2 + n;
		while (n--)
			*--s1 = *--s2;
	}
	else
		ft_memcpy(dest, src, n);
	return (dest);
}
