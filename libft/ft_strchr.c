#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] && s[i] != (char)c)
		i++;
	if (!s[i] && c != 0)
		return (NULL);
	return ((char *)s + i);
}
